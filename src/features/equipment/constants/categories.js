/**
 * Получить все категории как массив для селектов
 */
export const getCategoryOptions = () => {
  return Object.keys(EQUIPMENT_CATEGORIES).map(category => ({
    label: category,
    value: category
  }))
}

/**
 * Получить подкатегории для выбранной категории
 */
export const getSubcategoryOptions = (selectedCategory) => {
  if (!selectedCategory || !EQUIPMENT_CATEGORIES[selectedCategory]) {
    return []
  }
  
  return EQUIPMENT_CATEGORIES[selectedCategory].map(subcategory => ({
    label: subcategory,
    value: subcategory
  }))
}

/**
 * Проверить существует ли категория
 */
export const isCategoryValid = (category) => {
  return category && Object.keys(EQUIPMENT_CATEGORIES).includes(category)
}

/**
 * Проверить существует ли подкатегория в данной категории
 */
export const isSubcategoryValid = (category, subcategory) => {
  if (!isCategoryValid(category) || !subcategory) {
    return false
  }
  
  return EQUIPMENT_CATEGORIES[category].includes(subcategory)
}