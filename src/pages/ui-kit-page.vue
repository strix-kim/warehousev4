  const meta = item.meta.toLowerCase()
  
  // –ü–æ–∏—Å–∫ –ø–æ –Ω–∞–∑–≤–∞–Ω–∏—é, –æ–ø–∏—Å–∞–Ω–∏—é, –Ω–æ–º–µ—Ä—É
